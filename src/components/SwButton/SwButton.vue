<template>
  <Button
      :type="buttonType"
      :class="classes"
      :disabled="disabled || loading"
  >
    <Icon
        v-if="icon && iconOnLeft"
        :name="icon"
        data-testid="icon-left"
        class="icon-left no-padding"
    />
    <slot>Button text</slot>
    <Icon
        v-if="icon && !iconOnLeft"
        :name="icon"
        data-testid="icon-right"
        class="icon-right no-padding"
    />
  </Button>
</template>

<script>
import Icon from "../Icon/Icon.vue";

export const SIZES = { BIG: "big", SMALL: "small" };
export const THEMES = { PRIMARY: "primary", SECONDARY: "secondary", TERTIARY: "tertiary", SEMANTIC: "semantic" };
export const STATES = { DEFAULT: "default", DISABLED: "disabled", SUCCESS: "success", DESTRUCTIVE: "destructive" };

/**
 * SwButton
 */
export default {
  components: { Icon },
  props: {
    /**
     * The size of the button. Defaults to big.
     * @values big, small
     */
    size: { type: String, default: SIZES.BIG },
    /**
     * The Theme of the button. Defaults to primary
     * @values primary, secondary, link, semantic
     */
    theme: { type: String, default: THEMES.PRIMARY },
    /**
     * The State of the button. Defaults to default
     * @values default, disabled, success, destructive
     */
    state: { type: String, default: STATES.DEFAULT },
    /**
     * The Layout of the button. Defaults to text-only
     * @type {boolean}
     */
    iconOnLeft: { type: Boolean, default: false },
    /**
     * Flag for is button is loading
     * @type {boolean}
     */
    loading: { type: Boolean, default: false },
    /**
     * Flag for is button is disabled
     * @type {boolean}
     */
    disabled: { type: Boolean, default: false },
    /**
     * Flag for having no padding to the button
     */
    noPadding: { type: Boolean, default: false },
    /**
     * icon name
     * @values Any icon from Fontaweswimm library
     */
    icon: { type: String, default: null },
    /**
     * Button type
     * @values type attr of the button tag
     */
    buttonType: { type: String, default: "submit" },
  },
  computed: {
    classes() {
      return [
        "button",
        this.size,
        this.layout,
        this.state,
        this.theme,
        {
          "no-padding": this.noPadding,
          disabled: this.disabled,
          loading: this.loading,
        },
      ];
    },
  },
};
</script>

